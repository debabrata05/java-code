
public class Input 
{
	private int rollNo;
	private String name;
	private double marks;
	
	public int getRollNo() {
		return rollNo;
	}
	

	
	public void setRollNo(int rollNo) {
		this.rollNo = rollNo;
	}



	public void setName(String name) {
		this.name = name;
	}



	public void setMarks(double marks) {
		this.marks = marks;
	}



	public String getName() {
		return name;
	}
	
	
	public double getMarks() {
		return marks;
	}
	
	
	

}
