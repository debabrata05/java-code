package library;

public class SubscriberInput
{
	private int number;
	private String name;
	private String address;
	private int rentalItem;
	private int noOfSubscriber;
	
	public int getNoOfSubscriber() {
		return noOfSubscriber;
	}

	public void setNoOfSubscriber(int noOfSubscriber) {
		this.noOfSubscriber = noOfSubscriber;
	}

	public int getNumber() {
		return number;
	}
	
	public void setNumber(int number) {
		this.number = number;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getAddress() {
		return address;
	}
	
	public void setAddress(String address) {
		this.address = address;
	}
	
	public int getRentalItem() {
		return rentalItem;
	}
	
	public void setRentalItem(int rentalItem) {
		this.rentalItem = rentalItem;
	}
	
}
